//1. 조건을 여러개 쓰고 싶을 경우 else if를 사용합니다.
//2. else if는 여러 개 존재할 수 있습니다.
//3. else if 사용 시에는 반드시 if문이 존재해야 합니다.
//4. 만약에 else if 위에 적은 if문이나 else if문이 실행된다면 나머지는 실행되지 않습니다
