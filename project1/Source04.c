int main()
{
	//이 가게는 08 : 00 ~ 18 : 00까지 운영합니다.
	//운영하고 있으면 "OPEN", 아닐 경우 "CLOSE"를 화면에 출력하려고 합니다.

	int time = 9;

	//OPEN 할 조건? 8시부터 18시까지
	if (time >= 8)
	{
		//8시보다 크거나 같을 경우 작업 진행
		if (time <= 18)//18시보다 작거나 같으면 작업 진행
		{
			printf("OPEN\n");
		}
		else//18시보다 큰 경우 실행
		{
			printf("CLOSE\n");
		}
	}
	else//8시보다 작은 경우 작업진행
	{
		printf("CLOSE\n");
	}
	time = 19;

	// 두번째 방법 : 논리 연산자를 이용해 조건식 2개를 연결합니다.
	//c : 조건식
	//1. AND 연산	C1 && C2	C1의 조건과 C2의 조건이 둘다 만족해야 T
	//2. OR 연산	C1 || C2	C1의 조건이나 C2의 조건 중 하나라도 만족하면 T
	////3. NOT 연산	!C1			C1의 조건의 결과를 반대로 뽑습니다.		

	if (time >= 8 && time <= 18)//시간이 8시 이상 18시 이하
	{
		printf("OPEN\n");
	}
	else
	{
		printf("CLOSE\n");
	}
	//세번째 방법 조건 연산으로 설계하기
	//조건) 결과가 찬성/반대처럼 딱 두 가지로 나뉘는 경우에만 이 방식 사용 가능 가능
	time >= 8 && time <= 18 ? printf("OPEN=n") : printf("CLOSE\n");
	//시간이 8시 이상 18시 이하일 때
	//조건 맞으면 OPEN
	//조건 틀리면 CLOSE

	return 0;
}